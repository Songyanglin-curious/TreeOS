/**
 * Ascend 的运行状态外壳
 *
 * 说明：
 * - 外层不是“树本体”，而是为了手动推进方便而加的一层状态管理
 * - 真正的问题树在 tree 字段里
 */
export interface AscendState {
    /**
     * 当前这棵树正在解决的总问题
     *
     * 作用：
     * - 防止讨论过程中跑题
     * - 让每一轮都知道自己服务的是哪个总目标
     *
     * 例子：
     * "梳理三层任务下发 / 多天回送 / 归档统计这个复杂需求的真实业务结构"
     */
    rootQuestion: string;

    /**
     * 当前主线所在路径
     *
     * 作用：
     * - 标记“现在正在推进树里的哪个节点”
     * - 这是当前主线定位器
     *
     * 约定：
     * - 用标题路径表达，而不是 id
     * - 从根节点一路写到当前节点
     *
     * 例子：
     * ["系统事实主对象是什么"]
     * ["系统事实主对象是什么", "每日执行回送记录最少需要绑定哪些核心维度"]
     */
    currentPath: string[];

    /**
     * 下一轮默认推进路径
     *
     * 作用：
     * - 标记“下次最自然从哪里继续”
     * - 防止下一轮重新从头想
     *
     * 约定：
     * - 也是标题路径
     * - 如果当前轮还没确定下一轮，可先为空数组
     *
     * 例子：
     * ["系统事实主对象是什么", "每日执行回送记录最少需要绑定哪些核心维度"]
     */
    nextPath: string[];

    /**
     * 真正的问题树根节点
     *
     * 作用：
     * - 承载当前问题的递归结构
     * - children 会继续嵌套出子问题
     */
    tree: TreeItem;
}

/**
 * Ascend 的树节点
 *
 * 说明：
 * - 这是“树本体”的最小递归单元
 * - 一个节点只做四件事：
 *   1. 表示当前在讨论什么问题
 *   2. 保存这个问题当前冻结到哪
 *   3. 挂住暂时不推进但不能丢的问题
 *   4. 往下长出子问题
 */
export interface TreeItem {
    /**
     * 当前节点标题
     *
     * 作用：
     * - 表示“这个节点到底在问什么”
     * - 应尽量短、尽量像问题标题
     *
     * 例子：
     * "系统事实主对象是什么"
     * "每日执行回送记录最少需要绑定哪些核心维度"
     */
    title: string;

    /**
     * 当前节点的冻结结论
     *
     * 作用：
     * - 保存这个节点“目前推进到哪一步”
     * - 这是阶段性判断，不是终局答案
     * - 用来支持下一轮继续推进
     *
     * 建议：
     * - 尽量先压成一句话
     * - 不要把整轮分析过程都塞进来
     *
     * 例子：
     * "当前主线确定为：把每日执行回送记录作为系统事实主对象，再由它向上汇总出 B 视图和 A 视图。"
     */
    frozen: string;

    /**
     * 暂停区 / 停车场
     *
     * 作用：
     * - 挂住“已经发现但当前不推进”的内容
     * - 防止主线推进时把这些问题丢掉
     *
     * 这里适合放：
     * - 保留备选
     * - 当前不跟
     * - 待以后展开的问题
     * - 轻量回扫提示（如果你想先简单处理）
     *
     * 约定：
     * - 先用字符串轻量记录
     * - 只有当某个点真的要推进时，再把它升格成 children 里的正式节点
     *
     * 例子：
     * [
     *   "保留备选：以下发链路关系作为主对象，每日记录挂在其下",
     *   "当前不跟：权限细化、页面展示、统计口径细节、异常流程"
     * ]
     */
    parkingLot: string[];

    /**
     * 子节点列表
     *
     * 作用：
     * - 表示当前节点继续往下拆出的子问题
     * - 这才是树递归长下去的地方
     *
     * 使用原则：
     * - 只有“下一轮真的要推进”的问题，才放进 children
     * - 不要把所有想到的分支都立刻建成 children
     * - 暂时不推进的问题，优先放 parkingLot
     *
     * 例子：
     * [
     *   {
     *     title: "每日执行回送记录最少需要绑定哪些核心维度",
     *     frozen: "",
     *     parkingLot: [],
     *     children: []
     *   }
     * ]
     */
    children: TreeItem[];
}

/**
 * Ascend 状态最小模板
 */
export const ascendState: AscendState = {
    rootQuestion: "",
    currentPath: [],
    nextPath: [],
    tree: {
        title: "",
        frozen: "",
        parkingLot: [],
        children: []
    }
};

/**
 * 树节点最小模板
 */
export const treeItem: TreeItem = {
    title: "",
    frozen: "",
    parkingLot: [],
    children: []
};